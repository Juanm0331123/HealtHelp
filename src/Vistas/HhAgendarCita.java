    private String VerificacionFecha(String fecha) {
        String Verificar = "";
        if(fecha.equals("")){
            
        }
        else{
            // Obtener Fecha actual
        SimpleDateFormat dia = new SimpleDateFormat("dd");
        SimpleDateFormat mes = new SimpleDateFormat("MM");
        SimpleDateFormat Anio = new SimpleDateFormat("yyyy");
        java.util.Date FechaActual = new java.util.Date();
        DiaActual = Integer.parseInt(String.valueOf(dia.format(FechaActual)));
        MesActual = Integer.parseInt(String.valueOf(mes.format(FechaActual)));
        AnioActual = Integer.parseInt(String.valueOf(Anio.format(FechaActual)));

        //Obtener fecha seleccionada
        java.util.Date fechaSeleccionada = HhAgendarCita.RsCalendarDisponibilidad.getDatoFecha();
        SimpleDateFormat formatoDia = new SimpleDateFormat("dd");
        SimpleDateFormat formatoMes = new SimpleDateFormat("MM");
        SimpleDateFormat formatoAnio = new SimpleDateFormat("yyyy");

        DiaSeleccionado = Integer.parseInt(String.valueOf(formatoDia.format(fechaSeleccionada)));
        //JOptionPane.showMessageDialog(null, DiaSeleccionado);
        MesSeleccionado = Integer.parseInt(String.valueOf(formatoMes.format(fechaSeleccionada)));
        //JOptionPane.showMessageDialog(null, MesSeleccionado);
        AnioSeleccionado = Integer.parseInt(String.valueOf(formatoAnio.format(fechaSeleccionada)));
        //JOptionPane.showMessageDialog(null, AnioSeleccionado);
        //Condicional para escoger fecha Mayores
        
        if (AnioActual <= AnioSeleccionado) {
            if (MesActual <= MesSeleccionado) {
                if (DiaActual <= DiaSeleccionado) {

                    JOptionPane.showMessageDialog(null, "Fecha correcta");
                    Verificar = "Fecha correcta";

                } else {
                    JOptionPane.showMessageDialog(null, "Fecha incorrecta");
                    RsCalendarDisponibilidad.setDatoFecha(null);
                    Verificar = "Fecha incorrecta";
                }
            } else {
                JOptionPane.showMessageDialog(null, "Fecha incorrecta");
                RsCalendarDisponibilidad.setDatoFecha(null);
                Verificar = "Fecha incorrecta";
            }
        } else {
            JOptionPane.showMessageDialog(null, "Fecha incorrecta");
            RsCalendarDisponibilidad.setDatoFecha(null);
            Verificar = "Fecha incorrecta";

        }                        
        }    
        
        return Verificar;
    }
    

